/* Write an if statement, that takes our `input` value and assign to `result` a string `"Yes"` if input is `true`, or a `"No"` if input is `false`.

Console Log `result`
*/


const input = true;
let result; 

if (input === true) {
    result = "Yes";
} else {
    result = "No";
}

console.log (result);